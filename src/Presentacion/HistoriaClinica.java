    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        if(txtDni.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Campo DNI vacio", "Campo DNI vacio", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
            existePaciente=false;
            existeHC=false;
            for(int i=0; i<pacGral.pacientes().size(); i++){
                 
                if(pacGral.pacientes().get(i).getDni() == Long.parseLong(txtDni.getText())){
                    
                    indice = pacGral.pacientes().get(i).getIdPaciente();
                    txtApel.setText(pacGral.pacientes().get(i).getApellido());
                    txtNombre.setText(pacGral.pacientes().get(i).getNombre());
                    existePaciente=true;
                } 
            }
            if(existePaciente.equals(false)){
            
                JOptionPane.showMessageDialog(null, "DNI NO SE ENCUENTRA EN SISTEMA", "DNI INVALIDO", JOptionPane.ERROR_MESSAGE);
                txtApel.setText("");
                txtNombre.setText("");
                txaHist.setText("");                
            }else{
                for(int i=0; i<aHC.listarHC().size(); i++){
                    
                    if(aHC.listarHC().get(i).getIdPaciente() == indice){
                    txaHist.setText(aHC.listarHC().get(i).getNombre());
                    indiceHC = aHC.listarHC().get(i).getIdHistoriaClinica();
                    existeHC=true;
                }
            }
            
            
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Ingrese numeros", "Error #001", JOptionPane.ERROR_MESSAGE);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error general", "Error #001", JOptionPane.ERROR_MESSAGE);
        }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

