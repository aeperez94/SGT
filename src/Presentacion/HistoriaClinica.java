    private void btnActActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActActionPerformed
        // TODO add your handling code here:
        if( txaHist.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "NO SE PUDO OBTENER HISTORIA CLINICA", "HISTORIA CLINICA", JOptionPane.ERROR_MESSAGE);
        }else{
            if(existeHC.equals(true) && existePaciente.equals(true)){
                  aHC.atcHc(txaHist.getText(), indiceHC, indice); 
            }else{
                  JOptionPane.showMessageDialog(null, "NO SE PUDO ACTUALIZAR HC PORQUE NO SE ENCUENTRA EN BBDD", "HISTORIA CLINICA", JOptionPane.ERROR_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_btnActActionPerformed

